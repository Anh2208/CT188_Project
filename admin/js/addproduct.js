//xoá Item
function deleteItem(x) {
    let listItem = localStorage.getItem("list-item") ? JSON.parse(localStorage.getItem("list-item")) : []
    for (let i = 0; i < listItem.length; i++) {
        if (listItem[i].id == x) {
            listItem.splice(i, 1)
            localStorage.setItem("list-item", JSON.stringify(listItem))
            render()
        }
    }
}
